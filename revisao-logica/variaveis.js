// não pode "redeclarar" a variável
let teste = "minha string";
console.log(teste);

// pode "redeclarar" a variável, ex.: var teste = "oi" | var teste = "tchau"
var teste2 = "variável com var";
console.log(teste2);

//quando se declara uma constante é preciso já passar o valor da mesma
const teste3 = "valor imutável";
console.log(teste3);